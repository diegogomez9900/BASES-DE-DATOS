    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        conectar cc=new conectar();
        Connection cn = cc.conexion();
        String codigoo;
        
        //obtengo los valores guardados en los jcheck y cuento cuantos valores se han editado
        
        //aqui voy, falta validar cuales jcheckbox estan marcados para asi hacer la consulta, cambiando el string sql
        String sql;
        codigoo = codigo.getText();

        sql="DELETE FROM Infraccion WHERE Codigo_Infraccion = ?";

        try {
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1,codigoo);
            int n;
            n = pst.executeUpdate();

            if(n>0){
                JOptionPane.showMessageDialog(null,"Registro eliminado con exito");
                bloquear();
            }

        } catch (SQLException ex) {
            Logger.getLogger(infraccion_E.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

